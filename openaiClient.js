// タスク自動進行エンジン（Auto-task workflow engine）
//
// 各タスクの構造（task structure）:
//   id            : タスクの一意ID（string）
//   agentId       : 担当ID（例: "writer", "sns"）
//   instruction   : 担当への指示内容（string）
//   dependsOn     : 前工程タスクIDの配列（空配列なら最初に実行）
//   provider      : AIの種類（"openai" / 将来 "claude"）← AGENT_WORKFLOW_CONFIG から自動付与
//   enabled       : 有効・無効（true/false）← AGENT_WORKFLOW_CONFIG から自動付与
//   collaborators : 相談可能担当（将来の AI社員間相談用）← AGENT_WORKFLOW_CONFIG から自動付与
//   status        : 進行状態（pending / running / completed / error / skipped）
//   result        : 実行結果テキスト（string | null）
//   startedAt     : 実行開始日時（ISO 8601 文字列）
//   completedAt   : 実行完了日時（ISO 8601 文字列）
//
// 実行ルール：
//   1. dependsOn（前工程）が空のタスクから実行開始
//   2. 前工程が完了したタスクは自動で実行可能になる
//   3. 前工程の result（実行結果）は次のタスクの文脈として渡される
//   4. enabled: false の担当はスキップ
//   5. provider（AIの種類）は現在 "openai" のみ。将来 "claude" 対応予定
//
// 戻り値（return value）:
//   { workflowTasks, taskHistory }
//     workflowTasks : 実行済みタスクの配列
//     taskHistory   : タスク履歴（taskHistory）の配列
//       各履歴エントリの構造：
//         historyId    : 履歴の一意ID
//         from         : 依頼元担当ID（"leader" または前工程の agentId）
//         to           : 依頼先担当ID
//         taskId       : 対応するタスクID
//         instruction  : 指示内容
//         status       : 完了状態（completed / error / skipped）
//         requestedAt  : 依頼日時
//         completedAt  : 完了日時
// ══════════════════════════════════════════════════════════════
async function runAutoTaskWorkflow({ userMessage, tasks, autonomousConsult = false, workflowId = null, agentCaller = null, maxConsultations = 2 }) {
  // Phase39: ワークフロー全体の相談回数カウンター（最大 maxConsultations 回）
  let _consultCount = 0;
  // tasks に provider / enabled / collaborators / status / result / タイムスタンプ を付与
  const workflowTasks = tasks.map(t => {
    const config = AGENT_WORKFLOW_CONFIG[t.agentId] || {
      provider: 'openai',
      enabled: true,
      collaborators: [],
    };
    return {
      id: t.id,
      agentId: t.agentId,
      instruction: t.instruction || userMessage,
      dependsOn: Array.isArray(t.dependsOn) ? t.dependsOn : [],
      provider: config.provider,
      enabled: config.enabled !== false,
      collaborators: Array.isArray(config.collaborators) ? config.collaborators : [],
      status: 'pending',   // 進行状態（pending: 待機中）
      result: null,
      startedAt: null,
      completedAt: null,
    };
  });

  // taskHistory（タスク履歴）の配列。将来の画面表示・分析・AI間相談ログに使う
  const taskHistory = [];

  // enabled: false のタスクは最初に "skipped"（スキップ）にしておく
  workflowTasks.forEach(t => {
    if (!t.enabled) {
      t.status = 'skipped';
      taskHistory.push({
        historyId: `hist-${Date.now()}-${Math.random().toString(36).slice(2, 7)}`,
        from: 'leader',
        to: t.agentId,
        taskId: t.id,
        instruction: t.instruction,
        status: 'skipped',
        requestedAt: new Date().toISOString(),
        completedAt: new Date().toISOString(),
        note: 'enabled: false のためスキップ',
      });
    }
  });

  // 依存解決ループ（dependency resolution loop）
  const maxIterations = workflowTasks.length + 1;
  let iterations = 0;

  while (iterations < maxIterations) {
    iterations++;

    // 前工程が error のタスクを skipped にする（実行前スキップ処理）
    // 理由：エラーになった担当の出力を使って後続タスクを実行すると、
    //        空または誤った入力で実行が進む可能性があるため
    for (const t of workflowTasks) {
      if (t.status !== 'pending') continue;
      const errorDep = t.dependsOn
        .map(depId => workflowTasks.find(d => d.id === depId))
        .find(dep => dep && (dep.status === 'error' || dep.status === 'skipped'));
      if (errorDep) {
        const depProfile = LINE_AGENT_PROFILES[errorDep.agentId];
        const depName = depProfile ? depProfile.name : errorDep.agentId;
        t.status        = 'skipped';
        t.skippedReason = `前工程 ${depName} が ${errorDep.status} のためスキップ`;
        t.completedAt   = new Date().toISOString();
        taskHistory.push({
          historyId:   `hist-${Date.now()}-${Math.random().toString(36).slice(2, 7)}`,
          from:        errorDep.agentId,
          to:          t.agentId,
          taskId:      t.id,
          instruction: t.instruction,
          status:      'skipped',
          requestedAt: t.completedAt,
          completedAt: t.completedAt,
          note:        t.skippedReason,
        });
      }
    }

    // 実行可能なタスクを探す
    // 条件：status が "pending" かつ dependsOn（前工程）が全て "completed" または "skipped"
    const ready = workflowTasks.filter(t => {
      if (t.status !== 'pending') return false;
      return t.dependsOn.every(depId => {
        const dep = workflowTasks.find(d => d.id === depId);
        return dep && (dep.status === 'completed' || dep.status === 'skipped');
      });
    });

    if (ready.length === 0) break; // 実行可能なタスクがなければ終了

    // 実行可能なタスクを順番に実行
    for (const task of ready) {
      task.status = 'running';          // 進行状態を「実行中」に更新
      task.startedAt = new Date().toISOString();

      // taskHistory（タスク履歴）に「依頼元 → 依頼先」を記録
      // from（依頼元）: 前工程がある場合はその担当、なければ "leader"
      const fromAgentId = task.dependsOn.length > 0
        ? (workflowTasks.find(d => d.id === task.dependsOn[0])?.agentId || 'leader')
        : 'leader';
      const histEntry = {
        historyId:   `hist-${Date.now()}-${Math.random().toString(36).slice(2, 7)}`,
        from:        fromAgentId,
        to:          task.agentId,
        taskId:      task.id,
        instruction: task.instruction,
        status:      'running',
        requestedAt: task.startedAt,
        completedAt: null,
        workflowId:  workflowId || null,
        note:        '',
      };
      taskHistory.push(histEntry);

      try {
        // 前工程の result（実行結果）をコンテキストとして収集
        const prevResults = task.dependsOn
          .map(depId => workflowTasks.find(d => d.id === depId))
          .filter(dep => dep && dep.result)
          .map(dep => {
            const profile = LINE_AGENT_PROFILES[dep.agentId];
            const depName = profile ? profile.name : dep.agentId;
            return `【${depName}の成果】\n${dep.result}`;
          })
          .join('\n\n');

        // 指示内容に前工程の成果を付加
        const fullInstruction = prevResults
          ? `${task.instruction}\n\n---\n前工程の成果（参考にして作業すること）：\n${prevResults}`
          : task.instruction;

        // provider（AIの種類）に応じて実行
        // Phase37: agentCaller 経由で Claude / OpenAI を動的ルーティング
        let resultText = null;
        let taskProviderUsed = task.provider;
        let taskModelUsed    = null;
        let taskFallback     = false;
        const taskStartMs    = Date.now();

        const systemPrompt = buildSystemPrompt(task.agentId, null, null);
        if (agentCaller) {
          const cr = await agentCaller(task.agentId, systemPrompt, fullInstruction, []);
          resultText       = cr.text;
          taskProviderUsed = cr.provider;
          taskModelUsed    = cr.model   || null;
          taskFallback     = cr.fallback || false;
        } else {
          // agentCaller 未設定時は全担当 OpenAI（後方互換）
          resultText = await callOpenAI(systemPrompt, fullInstruction, []);
        }
        const taskResponseMs = Date.now() - taskStartMs;

        // JSON返答から reply 部分だけを取り出す（OpenAI 返答用）
        let cleanResult = resultText || '';
        if (taskProviderUsed !== 'claude' && taskProviderUsed !== 'claude_fallback') {
          try {
            const start = (resultText || '').indexOf('{');
            const end   = (resultText || '').lastIndexOf('}');
            if (start !== -1 && end > start) {
              const parsed = JSON.parse(resultText.slice(start, end + 1));
              if (parsed.reply) cleanResult = parsed.reply;
            }
          } catch (_) {}
        }

        task.result        = cleanResult;
        task.status        = 'completed';  // 進行状態を「完了」に更新
        task.completedAt   = new Date().toISOString();
        task.providerUsed  = taskProviderUsed;
        task.modelUsed     = taskModelUsed;
        task.responseMs    = taskResponseMs;
        task.fallback      = taskFallback;

        // taskHistory（タスク履歴）を「完了」に更新
        histEntry.status       = 'completed';
        histEntry.completedAt  = task.completedAt;
        histEntry.providerUsed = taskProviderUsed;
        histEntry.modelUsed    = taskModelUsed;
        histEntry.responseMs   = taskResponseMs;
        histEntry.fallback     = taskFallback;

        // ── Phase39: AI Collaboration Engine（自律相談）──────────────────
        // 条件: autonomousConsult ON / collaborators あり / 後処理タスクでない / 上限未達
        if (autonomousConsult && task.collaborators.length > 0 && !task.isPostProcess) {
          const myProfile   = LINE_AGENT_PROFILES[task.agentId];
          const myName      = myProfile ? myProfile.name : task.agentId;
          const myConfig    = AGENT_WORKFLOW_CONFIG[task.agentId] || {};
          const consultLevel = myConfig.consultLevel || 'LOW';
          task.autoConsultResults = [];

          // Phase39: consultLevel と回答内容から相談要否判定
          const needConsult = shouldAutoConsult(cleanResult, consultLevel);

          for (const consultTargetId of task.collaborators) {
            // 基本スキップ条件
            if (consultTargetId === task.agentId) continue;
            const consultCfg = AGENT_WORKFLOW_CONFIG[consultTargetId];
            if (!consultCfg || consultCfg.enabled === false) continue;

            // Phase39: 最大相談回数チェック（ワークフロー全体）
            if (_consultCount >= maxConsultations) {
              task.autoConsultResults.push({
                targetId: consultTargetId, targetName: consultTargetId,
                reply: '', status: 'skipped',
                skipReason: `最大相談回数（${maxConsultations}回）に達したためスキップ`,
              });
              continue;
            }

            // Phase39: 相談不要と判定した場合はスキップ（トークン節約）
            if (!needConsult) {
              task.autoConsultResults.push({
                targetId: consultTargetId,
                targetName: (LINE_AGENT_PROFILES[consultTargetId] || {}).name || consultTargetId,
                reply: '', status: 'skipped',
                skipReason: `consultLevel=${consultLevel}：相談不要と判定`,
              });
              continue;
            }

            const targetProfile = LINE_AGENT_PROFILES[consultTargetId];
            const targetName    = targetProfile ? targetProfile.name : consultTargetId;
            const consultReason = consultLevel === 'HIGH' ? '積極相談（HIGH）'
              : consultLevel === 'MEDIUM' ? '品質向上のため相談（MEDIUM）'
              : '不確かさ検知（LOW）';

            const acHistEntry = {
              historyId:     `hist-${Date.now()}-${Math.random().toString(36).slice(2, 7)}`,
              from:          task.agentId,
              to:            consultTargetId,
              taskId:        task.id,
              action:        'auto_consult',
              instruction:   task.instruction,
              status:        'running',
              requestedAt:   new Date().toISOString(),
              completedAt:   null,
              workflowId:    workflowId || null,
              consultLevel,
              consultReason,
              note:          `AI Collaboration（${myName} → ${targetName}）[${consultLevel}]`,
            };
            taskHistory.push(acHistEntry);

            try {
              const acSystemPrompt = buildSystemPrompt(consultTargetId, null, null);
              const acQuestion = [
                `【依頼内容】${task.instruction}`,
                ``,
                `【${myName}の提案（案）】`,
                cleanResult,
                ``,
                `この提案についてあなたの専門的な観点からフィードバック・補足・改善点を簡潔に提示してください。`,
              ].join('\n');

              // Phase39: agentCaller 経由で Provider 設定を維持
              const acStart = Date.now();
              let acText = '';
              let acProvider = consultCfg.provider || 'openai';
              let acModel    = null;
              let acFallback = false;

              if (agentCaller) {
                const acCr = await agentCaller(consultTargetId, acSystemPrompt, acQuestion, []);
                acText     = acCr.text || '';
                acProvider = acCr.provider;
                acModel    = acCr.model || null;
                acFallback = acCr.fallback || false;
              } else {
                let acReply = await callOpenAI(acSystemPrompt, acQuestion, []);
                acText = acReply || '';
                try {
                  const as = (acReply || '').indexOf('{');
                  const ae = (acReply || '').lastIndexOf('}');
                  if (as !== -1 && ae > as) {
                    const p = JSON.parse(acReply.slice(as, ae + 1));
                    if (p.reply) acText = p.reply;
                  }
                } catch (_) {}
              }
              const acMs = Date.now() - acStart;

              task.result += `\n\n---\n💬 【AI Collaboration：${targetName}より】\n${acText}`;
              task.autoConsultResults.push({
                targetId: consultTargetId, targetName, reply: acText, status: 'completed',
                consultLevel, consultReason, providerUsed: acProvider, modelUsed: acModel,
                responseMs: acMs, fallback: acFallback,
              });

              acHistEntry.status       = 'completed';
              acHistEntry.completedAt  = new Date().toISOString();
              acHistEntry.providerUsed = acProvider;
              acHistEntry.modelUsed    = acModel;
              acHistEntry.responseMs   = acMs;
              acHistEntry.fallback     = acFallback;

              _consultCount++; // Phase39: 相談カウンター加算

            } catch (acErr) {
              task.autoConsultResults.push({
                targetId: consultTargetId, targetName, reply: '', status: 'skipped',
                consultLevel, skipReason: `エラー: ${acErr.message}`,
              });
              acHistEntry.status      = 'skipped';
              acHistEntry.completedAt = new Date().toISOString();
              acHistEntry.note        = `Collaborationエラー: ${acErr.message}`;
            }
          }

          task.autoConsultStatus = task.autoConsultResults.some(r => r.status === 'completed')
            ? 'completed' : 'skipped';
          task.consultCount = _consultCount; // 参照用（現時点の合計）
        }
        // ── AI Collaboration Engine ここまで ─────────────────────────────

      } catch (e) {
        task.status      = 'error';
        task.result      = `エラー: ${e.message}`;
        task.completedAt = new Date().toISOString();

        histEntry.status      = 'error';
        histEntry.completedAt = task.completedAt;
        histEntry.note        = e.message;
      }
    }
  }

  // ── Phase 6: 後処理 Reviewer → Strategy ─────────────────────────────
  // 担当タスクが1件以上完了した場合のみ実行
  // Reviewer・Strategy は isPostProcess=true のため autonomousConsult しない（無限ループ防止）
  const completedMainTasks = workflowTasks.filter(t => t.status === 'completed' && t.result && !t.isPostProcess);

  if (completedMainTasks.length > 0) {

    // ─ Reviewer（品質・抜け漏れ・矛盾チェック）─────────────────────────
    const reviewerCfg  = AGENT_WORKFLOW_CONFIG['reviewer'] || { provider: 'openai' };
    const reviewerTask = {
      id:            'at-postprocess-reviewer',
      agentId:       'reviewer',
      instruction:   userMessage,
      dependsOn:     [],
      provider:      reviewerCfg.provider,
      enabled:       true,
      collaborators: [],        // Reviewer は相談しない
      status:        'running',
      result:        null,
      startedAt:     new Date().toISOString(),
      completedAt:   null,
      isPostProcess: true,      // 後処理フラグ（autonomousConsult 対象外）
    };
    workflowTasks.push(reviewerTask);

    const rvHist = {
      historyId:   `hist-${Date.now()}-${Math.random().toString(36).slice(2, 7)}`,
      from:        'leader',
      to:          'reviewer',
      taskId:      reviewerTask.id,
      action:      'reviewer',
      instruction: userMessage,
      status:      'running',
      requestedAt: reviewerTask.startedAt,
      completedAt: null,
      workflowId:  workflowId || null,
      note:        'Phase6 Reviewer：品質・抜け漏れ・矛盾チェック',
    };
    taskHistory.push(rvHist);

    try {
      // 各担当のメイン成果（自律相談の追記部分を除く）を収集
      const mainResultsText = completedMainTasks.map(t => {
        const p        = LINE_AGENT_PROFILES[t.agentId];
        const mainPart = (t.result || '').split('\n\n---\n')[0]; // 自律相談追記前の本文
        return `【${p ? p.name : t.agentId}】\n${mainPart}`;
      }).join('\n\n---\n');

      const rvSystemPrompt = buildSystemPrompt('reviewer', null, null);
      const rvQuestion     = [
        `【元の依頼】${userMessage}`,
        ``,
        `【各担当の成果物】`,
        mainResultsText,
        ``,
        `以下の観点でレビューしてください（箇条書き）：`,
        `①抜け漏れ（依頼に対して不足している点）`,
        `②品質（各担当の成果物の完成度）`,
        `③矛盾（担当間で内容が食い違っている点）`,
      ].join('\n');

      let rvReply;
      const rvStartMs = Date.now();
      if (agentCaller) {
        const rvCr = await agentCaller('reviewer', rvSystemPrompt, rvQuestion, []);
        rvReply = rvCr.text;
        reviewerTask.providerUsed = rvCr.provider;
        reviewerTask.modelUsed    = rvCr.model   || null;
        reviewerTask.fallback     = rvCr.fallback || false;
      } else {
        rvReply = await callOpenAI(rvSystemPrompt, rvQuestion, []);
        reviewerTask.providerUsed = 'openai';
      }
      reviewerTask.responseMs = Date.now() - rvStartMs;
      let rvText = rvReply || '';
      try {
        const rs = (rvReply || '').indexOf('{');
        const re = (rvReply || '').lastIndexOf('}');
        if (rs !== -1 && re > rs) {
          const p = JSON.parse(rvReply.slice(rs, re + 1));
          // reply フィールドに限らず、最も長い文字列フィールドを使用
          const longest = Object.values(p)
            .filter(v => typeof v === 'string')
            .sort((a, b) => b.length - a.length)[0];
          if (longest && longest.length > 10) rvText = longest;
        }
      } catch (_) {}
      // JSON.parse 失敗時（実改行を含むJSONなど）: reply フィールドを文字列抽出
      if (rvText.trimStart().startsWith('{') && rvText.includes('"reply"')) {
        const ri = rvText.indexOf('"reply"');
        const qi = rvText.indexOf('"', ri + 7) + 1;
        if (qi > 1) {
          let ci = qi;
          while (ci < rvText.length) {
            if (rvText[ci] === '"' && rvText[ci - 1] !== '\\') break;
            ci++;
          }
          const extracted = rvText.slice(qi, ci).replace(/\\n/g, '\n').replace(/\\"/g, '"');
          if (extracted.length > 10) rvText = extracted;
        }
      }

      reviewerTask.result      = rvText;
      reviewerTask.status      = 'completed';
      reviewerTask.completedAt = new Date().toISOString();
      rvHist.status            = 'completed';
      rvHist.completedAt       = reviewerTask.completedAt;

    } catch (rvErr) {
      reviewerTask.status      = 'error';
      reviewerTask.result      = `Reviewerエラー（スキップ）: ${rvErr.message}`;
      reviewerTask.completedAt = new Date().toISOString();
      rvHist.status            = 'error';
      rvHist.completedAt       = reviewerTask.completedAt;
      rvHist.note              = rvErr.message;
    }

    // ─ Strategy（優先順位・改善案・リスク統合）────────────────────────
    const strategyCfg  = AGENT_WORKFLOW_CONFIG['strategy'] || { provider: 'openai' };
    const strategyTask = {
      id:            'at-postprocess-strategy',
      agentId:       'strategy',
      instruction:   userMessage,
      dependsOn:     [],
      provider:      strategyCfg.provider,
      enabled:       true,
      collaborators: [],        // Strategy は相談しない
      status:        'running',
      result:        null,
      startedAt:     new Date().toISOString(),
      completedAt:   null,
      isPostProcess: true,
    };
    workflowTasks.push(strategyTask);

    const stHist = {
      historyId:   `hist-${Date.now()}-${Math.random().toString(36).slice(2, 7)}`,
      from:        'reviewer',
      to:          'strategy',
      taskId:      strategyTask.id,
      action:      'strategy',
      instruction: userMessage,
      status:      'running',
      requestedAt: strategyTask.startedAt,
      completedAt: null,
      workflowId:  workflowId || null,
      note:        'Phase6 Strategy：優先順位・改善案・リスク統合',
    };
    taskHistory.push(stHist);

    try {
      const reviewerOutput = reviewerTask.result || '（Reviewerのフィードバックなし）';
      const summaryText    = completedMainTasks.map(t => {
        const p        = LINE_AGENT_PROFILES[t.agentId];
        const mainPart = (t.result || '').split('\n\n---\n')[0];
        return `【${p ? p.name : t.agentId}】${mainPart.slice(0, 200)}…`;
      }).join('\n');

      const stSystemPrompt = buildSystemPrompt('strategy', null, null);
      const stQuestion     = [
        `【元の依頼】${userMessage}`,
        ``,
        `【各担当の成果物（要約）】`,
        summaryText,
        ``,
        `【Reviewerのフィードバック】`,
        reviewerOutput,
        ``,
        `以下の観点で統合・提言してください（箇条書き）：`,
        `①優先順位（最初に実行すべきことは何か）`,
        `②改善案（Reviewerの指摘を受けての具体的な改善点）`,
        `③リスク（このまま進む場合の懸念点）`,
      ].join('\n');

      let stReply;
      const stStartMs = Date.now();
      if (agentCaller) {
        const stCr = await agentCaller('strategy', stSystemPrompt, stQuestion, []);
        stReply = stCr.text;
        strategyTask.providerUsed = stCr.provider;
        strategyTask.modelUsed    = stCr.model   || null;
        strategyTask.fallback     = stCr.fallback || false;
      } else {
        stReply = await callOpenAI(stSystemPrompt, stQuestion, []);
        strategyTask.providerUsed = 'openai';
      }
      strategyTask.responseMs = Date.now() - stStartMs;
      let stText = stReply || '';
      try {
        const ss = (stReply || '').indexOf('{');
        const se = (stReply || '').lastIndexOf('}');
        if (ss !== -1 && se > ss) {
          const p = JSON.parse(stReply.slice(ss, se + 1));
          const longest = Object.values(p)
            .filter(v => typeof v === 'string')
            .sort((a, b) => b.length - a.length)[0];
          if (longest && longest.length > 10) stText = longest;
        }
      } catch (_) {}
      // JSON.parse 失敗時: "reply":"..." の中身を文字列抽出
      if (stText.trimStart().startsWith('{') && stText.includes('"reply"')) {
        const ri = stText.indexOf('"reply"');
        const qi = stText.indexOf('"', ri + 7) + 1;
        if (qi > 1) {
          let ci = qi;
          while (ci < stText.length) {
            if (stText[ci] === '"' && stText[ci - 1] !== '\\') break;
            ci++;
          }
          const extracted = stText.slice(qi, ci).replace(/\\n/g, '\n').replace(/\\"/g, '"');
          if (extracted.length > 10) stText = extracted;
        }
      }

      strategyTask.result      = stText;
      strategyTask.status      = 'completed';
      strategyTask.completedAt = new Date().toISOString();
      stHist.status            = 'completed';
      stHist.completedAt       = strategyTask.completedAt;

    } catch (stErr) {
      strategyTask.status      = 'error';
      strategyTask.result      = `Strategyエラー（スキップ）: ${stErr.message}`;
      strategyTask.completedAt = new Date().toISOString();
      stHist.status            = 'error';
      stHist.completedAt       = strategyTask.completedAt;
      stHist.note              = stErr.message;
    }
  }
  // ── 後処理ここまで ────────────────────────────────────────────────────

  return { workflowTasks, taskHistory };
}

