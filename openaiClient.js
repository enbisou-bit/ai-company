// memberData引数でname/role/descriptionを受け取れる（サーバーから渡す場合）
function autoGenerateProfile(memberId, memberData = {}) {
  const externalName = memberData.name || '';
  const externalRole = memberData.role || '';
  const externalDesc = memberData.description || '';

  // 外部データがあればそれを優先、なければIDから推定
  let guessedRole = externalRole || (memberId + '専門家');
  if (!externalRole) {
    for (const [key, role] of Object.entries(AUTO_ROLE_MAP)) {
      if (memberId.includes(key)) { guessedRole = role; break; }
    }
  }

  const displayName = externalName || (memberId.includes('担当') ? memberId : `${memberId}担当`);
  const descContext = externalDesc ? `\n専門背景：${externalDesc}` : '';

  console.log(`[autoGenerateProfile] 未登録担当 "${memberId}" → 動的生成: ${guessedRole}`);

  return {
    name: displayName,
    role: guessedRole,
    background: `${guessedRole}として実務経験を持つプロフェッショナル。${externalDesc}`,
    tone: `${guessedRole}の専門家として、①現状分析②仮説提示③不足情報確認④提案の順で回答する。数値と根拠で語る。`,
    persona: `【性格】${guessedRole}のプロフェッショナル。専門外は他担当へ誘導する。${descContext}\n【禁止】専門外の深い回答。根拠なし提案。一般論のみの回答。`,
    domain: `${guessedRole}として専門的な視点から提案を行うこと。専門外の質問は「○○担当へ依頼推奨」と返すこと。`,
    fixedSuggestions: null,
  };
}

