// ── Strategy 単独会話監視 ────────────────────────────────────
async function strategyMonitor({ userMessage, aiReply, memberId }) {
  if (!OPENAI_API_KEY || !costTracker.canProcess()) return null;
  const context = `【担当】${memberId}\n【ユーザー】${userMessage}\n【担当の返答】${aiReply}`;
  const text = await callOpenAI(buildStrategyMonitorPrompt(), context);
  if (!text) return null;
  try {
    const start = text.indexOf('{');
    const end = findBalancedEnd(text, start);
    if (start === -1 || end <= start) return null;
    const parsed = JSON.parse(text.slice(start, end + 1));
    return parsed.intervene === true ? parsed : null;
  } catch {
    return null;
  }
}

// ── 対応括弧カウンタ（}}}対策）──────────────────────────────
function findBalancedEnd(str, startIdx) {
  let depth = 0, inStr = false;
  for (let i = startIdx; i < str.length; i++) {
    const c = str[i];
    if (inStr) {
      if (c === '\\') { i++; continue; }
      if (c === '"') inStr = false;
    } else {
      if (c === '"') { inStr = true; continue; }
      if (c === '{') depth++;
      else if (c === '}') { depth--; if (depth === 0) return i; }
    }
  }
  return str.lastIndexOf('}');
}

